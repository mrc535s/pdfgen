function mapObj(o) {
  return reducer(o);
}

function reducer(x) {
  var keys = getKeys(x);
  return mapper(keys, x);
}

function getKeys(obj) {
  return Object.keys(obj);
}

function mapper(y, x) {
  return y.map(function (k) {
    if (typeof x[k] === "object") {
      return [k, reducer(x[k])];
    }
    return [k, x[k]]
  });
}

